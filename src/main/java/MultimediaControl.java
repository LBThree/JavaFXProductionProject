/**
 * An interface that describes that actions that an audio player or movie player can use.
 */
public interface MultimediaControl {

  /**
   * To be used for console output.
   *
   * @deprecated not used
   */
  void play();

  /**
   * To be used for console output.
   *
   * @deprecated not used
   */
  void stop();

  /**
   * To be used for console output.
   *
   * @deprecated not used
   */
  void previous();

  /**
   * To be used for console output.
   *
   * @deprecated not used
   */
  void next();
}
